// Language switch functionality
function switchLanguage(lang) {
    const allElements = document.querySelectorAll('[class^="lang-"]');
    allElements.forEach(el => {
        if (el.classList.contains(`lang-${lang}`)) {
            el.style.display = '';
        } else {
            el.style.display = 'none';
        }
    });
}

